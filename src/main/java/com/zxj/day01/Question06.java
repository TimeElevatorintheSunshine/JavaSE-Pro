package com.zxj.day01;

/**
 * 在java中，算术运算符加号“+”，也可以作为字符串的连接符。它可以将多个字符串连接在一起，组成一个新的字符串，也可以将其他数据类型与字符串连接在一起，组成一个新的字符串。请阅读如下解释：
 */
public class Question06 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(a + " + " + b + " = " + c);
    }
}
